public class pattern {
    public static void main(String[] args) {
        //Square Pattern
       for(int line=1; line<=4 ; line++){
        System.out.println("****");
       }
   // Sqaure Pattern by using while loop
       int sq =1;
       while(sq<=4){
        System.out.println("****");
        sq++;
       }
       
    }
}
